def _parse_image_element(element: Any, style_dict: Dict[str, str]) -> Optional[ImageElement]:
    """Parse an <image> element into an ImageElement."""
    try:
        x = float(element.get("x", 0))
        y = float(element.get("y", 0))
        width = float(element.get("width", 0))
        height = float(element.get("height", 0))

        # Prefer href (SVG2) over xlink:href (classic)
        src = element.get("href") or element.get("{http://www.w3.org/1999/xlink}href", "")

        opacity = _get_numeric_style(element, style_dict, "opacity")

        attrs = _extract_base_attrs(element, style_dict)
        if opacity is not None:
            attrs["opacity"] = opacity

        return ImageElement(
            type="image",
            src=src,
            position=[x, y],
            size=[width, height],
            **attrs,
        )
    except Exception as e:
        raise ParseError(f"Failed to parse <image> element: {e}")


def _parse_group_element(element: Any) -> Optional[GroupElement]:
    """Parse a <g> (group) element into a GroupElement, recursively parsing children."""
    try:
        style_dict = _parse_style_dict(element.get("style"))
        children = []

        for child in element:
            # Skip non-element nodes and <defs>
            if not isinstance(child.tag, str):
                continue
            tag = child.tag.split("}")[-1] if "}" in child.tag else child.tag
            if tag == "defs":
                continue

            parsed = _parse_element(child)
            if parsed:
                children.append(parsed)

        return GroupElement(
            type="group",
            children=children,
            **_extract_base_attrs(element, style_dict),
        )
    except Exception as e:
        raise ParseError(f"Failed to parse <g> (group) element: {e}")


def _parse_element(element: Any) -> Optional[Element]:
    """Dispatch an element to its parser based on tag name."""
    if not isinstance(element.tag, str):
        return None

    tag = element.tag.split("}")[-1] if "}" in element.tag else element.tag
    style_dict = _parse_style_dict(element.get("style"))

    # Skip background rectangles with id="background"
    if tag == "rect" and element.get("id") == "background":
        return None

    if tag == "text":
        return _parse_text_element(element, style_dict)
    elif tag == "rect":
        return _parse_rect_element(element, style_dict)
    elif tag == "circle":
        return _parse_circle_element(element, style_dict)
    elif tag == "ellipse":
        return _parse_ellipse_element(element, style_dict)
    elif tag == "line":
        return _parse_line_element(element, style_dict)
    elif tag == "polyline":
        return _parse_polyline_element(element, style_dict)
    elif tag == "polygon":
        return _parse_polygon_element(element, style_dict)
    elif tag == "path":
        return _parse_path_element(element, style_dict)
    elif tag == "image":
        return _parse_image_element(element, style_dict)
    elif tag == "g":
        return _parse_group_element(element)
    else:
        return None


def _extract_gradients(defs: Any) -> Dict[str, Any]:
    """Extract linear and radial gradients from <defs> element."""
    gradients = {}

    for child in defs:
        if not isinstance(child.tag, str):
            continue
        tag = child.tag.split("}")[-1] if "}" in child.tag else child.tag

        if tag == "linearGradient":
            grad_id = child.get("id")
            if grad_id:
                x1 = float(child.get("x1", 0))
                y1 = float(child.get("y1", 0))
                x2 = float(child.get("x2", 1))
                y2 = float(child.get("y2", 0))
                stops = []

                for stop in child:
                    stop_tag = stop.tag.split("}")[-1] if "}" in stop.tag else stop.tag
                    if stop_tag == "stop":
                        offset = float(stop.get("offset", 0).rstrip("%"))
                        stop_color = stop.get("stop-color", "#000000")
                        stop_opacity = float(stop.get("stop-opacity", 1))
                        stops.append(StopSpec(offset=offset, color=stop_color, opacity=stop_opacity))

                if stops:
                    gradients[grad_id] = LinearGradientSpec(
                        type="linear",
                        x1=x1,
                        y1=y1,
                        x2=x2,
                        y2=y2,
                        stops=stops,
                    )

        elif tag == "radialGradient":
            grad_id = child.get("id")
            if grad_id:
                cx = float(child.get("cx", 0.5))
                cy = float(child.get("cy", 0.5))
                r = float(child.get("r", 0.5))
                fx = child.get("fx")
                fy = child.get("fy")

                stops = []
                for stop in child:
                    stop_tag = stop.tag.split("}")[-1] if "}" in stop.tag else stop.tag
                    if stop_tag == "stop":
                        offset = float(stop.get("offset", 0).rstrip("%"))
                        stop_color = stop.get("stop-color", "#000000")
                        stop_opacity = float(stop.get("stop-opacity", 1))
                        stops.append(StopSpec(offset=offset, color=stop_color, opacity=stop_opacity))

                if stops:
                    grad_dict = {
                        "type": "radial",
                        "cx": cx,
                        "cy": cy,
                        "r": r,
                        "stops": stops,
                    }
                    if fx:
                        grad_dict["fx"] = float(fx)
                    if fy:
                        grad_dict["fy"] = float(fy)
                    gradients[grad_id] = RadialGradientSpec(**grad_dict)

    return gradients


def _extract_clip_paths(defs: Any) -> Dict[str, Any]:
    """Extract clip paths from <defs> element (simplified parsing)."""
    clips = {}
    
    for child in defs:
        if not isinstance(child.tag, str):
            continue
        tag = child.tag.split("}")[-1] if "}" in child.tag else child.tag

        if tag == "clipPath":
            clip_id = child.get("id")
            if clip_id:
                # For now, extract the first shape from the clip path
                for shape in child:
                    if not isinstance(shape.tag, str):
                        continue
                    shape_tag = shape.tag.split("}")[-1] if "}" in shape.tag else shape.tag
                    style_dict = _parse_style_dict(shape.get("style"))

                    if shape_tag == "rect":
                        x = float(shape.get("x", 0))
                        y = float(shape.get("y", 0))
                        width = float(shape.get("width", 0))
                        height = float(shape.get("height", 0))
                        fill = _get_paint_color(shape, "fill", style_dict)
                        stroke = _extract_stroke(shape, style_dict)

                        clips[clip_id] = RectGeo(
                            type="rect",
                            position=[x, y],
                            size=[width, height],
                            fill=fill,
                            stroke=stroke,
                        )
                        break

                    elif shape_tag == "circle":
                        cx = float(shape.get("cx", 0))
                        cy = float(shape.get("cy", 0))
                        radius = float(shape.get("r", 0))
                        fill = _get_paint_color(shape, "fill", style_dict)
                        stroke = _extract_stroke(shape, style_dict)

                        clips[clip_id] = CircleGeo(
                            type="circle",
                            center=[cx, cy],
                            radius=radius,
                            fill=fill,
                            stroke=stroke,
                        )
                        break

    return clips


def _extract_filters(defs: Any) -> Dict[str, Any]:
    """Extract SVG filters from <defs> element (simplified parsing)."""
    filters = {}

    for child in defs:
        if not isinstance(child.tag, str):
            continue
        tag = child.tag.split("}")[-1] if "}" in child.tag else child.tag

        if tag == "filter":
            filter_id = child.get("id")
            if filter_id:
                primitives = []

                for prim in child:
                    if not isinstance(prim.tag, str):
                        continue
                    prim_tag = prim.tag.split("}")[-1] if "}" in prim.tag else prim.tag

                    if prim_tag == "feGaussianBlur":
                        std_dev = prim.get("stdDeviation", "1")
                        try:
                            std_vals = [float(v) for v in std_dev.split()]
                            std_dev_val = std_vals if len(std_vals) > 1 else std_vals[0]
                        except ValueError:
                            std_dev_val = 1.0
                        primitives.append(GaussianBlurPrimitive(type="gaussianBlur", std_deviation=std_dev_val))

                    elif prim_tag == "feOffset":
                        dx = float(prim.get("dx", 0))
                        dy = float(prim.get("dy", 0))
                        primitives.append(OffsetPrimitive(type="offset", dx=dx, dy=dy))

                    elif prim_tag == "feColorMatrix":
                        mode = prim.get("type", "saturate")
                        values = prim.get("values")
                        primitives.append(ColorMatrixPrimitive(type="colorMatrix", mode=mode, values=values))

                    elif prim_tag == "feBlend":
                        mode = prim.get("mode", "normal")
                        in2 = prim.get("in2", "SourceGraphic")
                        primitives.append(BlendPrimitive(type="blend", mode=mode, in2=in2))

                    elif prim_tag == "feMerge":
                        nodes = []
                        for merge_node in prim:
                            node_tag = merge_node.tag.split("}")[-1] if "}" in merge_node.tag else merge_node.tag
                            if node_tag == "feMergeNode":
                                in_attr = merge_node.get("in", "SourceGraphic")
                                nodes.append(in_attr)
                        primitives.append(MergePrimitive(type="merge", nodes=nodes))

                if primitives:
                    filters[filter_id] = FilterSpec(primitives=primitives)

    return filters


def _extract_masks(defs: Any) -> Dict[str, Any]:
    """Extract SVG masks from <defs> element (simplified parsing)."""
    masks = {}

    for child in defs:
        if not isinstance(child.tag, str):
            continue
        tag = child.tag.split("}")[-1] if "}" in child.tag else child.tag

        if tag == "mask":
            mask_id = child.get("id")
            if mask_id:
                mask_type = child.get("mask-type")
                shapes = []

                for shape in child:
                    if not isinstance(shape.tag, str):
                        continue
                    shape_tag = shape.tag.split("}")[-1] if "}" in shape.tag else shape.tag
                    style_dict = _parse_style_dict(shape.get("style"))

                    if shape_tag == "rect":
                        x = float(shape.get("x", 0))
                        y = float(shape.get("y", 0))
                        width = float(shape.get("width", 0))
                        height = float(shape.get("height", 0))
                        fill = _get_paint_color(shape, "fill", style_dict)
                        stroke = _extract_stroke(shape, style_dict)

                        shapes.append(RectGeo(
                            type="rect",
                            position=[x, y],
                            size=[width, height],
                            fill=fill,
                            stroke=stroke,
                        ))

                    elif shape_tag == "circle":
                        cx = float(shape.get("cx", 0))
                        cy = float(shape.get("cy", 0))
                        radius = float(shape.get("r", 0))
                        fill = _get_paint_color(shape, "fill", style_dict)
                        stroke = _extract_stroke(shape, style_dict)

                        shapes.append(CircleGeo(
                            type="circle",
                            center=[cx, cy],
                            radius=radius,
                            fill=fill,
                            stroke=stroke,
                        ))

                if shapes:
                    masks[mask_id] = MaskSpec(type=mask_type, shapes=shapes)

    return masks


def parse_svg_to_design(svg_path: Union[str, Path]) -> Design:
    """Parse an SVG file and return a Design object.

    Args:
        svg_path: Path to the SVG file.

    Returns:
        A validated Design object with full support for:
        - All SVG element types (shapes, text, images, groups)
        - Styling (fill, stroke, opacity, transforms)
        - Gradients (linear and radial)
        - Filters (Gaussian blur, offset, color matrix, blend, merge)
        - Clip paths and masks

    Raises:
        ParseError: If the SVG cannot be parsed or is malformed.
    """
    try:
        tree = etree.parse(str(svg_path))
        root = tree.getroot()
    except Exception as e:
        raise ParseError(f"Failed to parse SVG file {svg_path}: {e}")

    try:
        width, height = _extract_viewbox_and_size(root)
        size_str = f"{int(width)}x{int(height)}"
    except Exception as e:
        raise ParseError(f"Failed to extract canvas size: {e}")

    # Extract background color if present
    background = None
    style_dict = _parse_style_dict(root.get("style"))
    bg = root.get("fill") or style_dict.get("fill")
    if bg and bg.lower() != "none":
        background = bg

    # Extract definitions (gradients, filters, clips, masks)
    gradients = {}
    filters = {}
    clips = {}
    masks = {}

    for defs in root.findall(".//{http://www.w3.org/2000/svg}defs"):
        gradients.update(_extract_gradients(defs))
        filters.update(_extract_filters(defs))
        clips.update(_extract_clip_paths(defs))
        masks.update(_extract_masks(defs))

    # Also check for non-namespaced defs
    for defs in root.findall(".//defs"):
        gradients.update(_extract_gradients(defs))
        filters.update(_extract_filters(defs))
        clips.update(_extract_clip_paths(defs))
        masks.update(_extract_masks(defs))

    # Parse elements (skip <defs> and any non-visual elements)
    elements: List[Element] = []
    for child in root:
        if not isinstance(child.tag, str):
            continue
        tag = child.tag.split("}")[-1] if "}" in child.tag else child.tag
        if tag == "defs":
            continue
        parsed = _parse_element(child)
        if parsed:
            elements.append(parsed)

    design_name = root.get("id") or "Parsed Design"

    return Design(
        design=design_name,
        size=size_str,
        background=background,
        gradients=gradients,
        filters=filters,
        clips=clips,
        masks=masks,
        elements=elements,
    )


def svg_to_yaml(svg_path: Union[str, Path], output_path: Optional[Union[str, Path]] = None) -> str:
    """Parse an SVG file and output InkLang YAML.

    Args:
        svg_path: Path to the input SVG file.
        output_path: Optional path to write the YAML. If None, returns the YAML string.

    Returns:
        YAML string representation of the design.

    Raises:
        ParseError: If the SVG cannot be parsed.
    """
    design = parse_svg_to_design(svg_path)

    # Convert to dict for YAML serialization
    design_dict = design.model_dump(exclude_none=True, by_alias=False)

    yaml_str = yaml.dump(design_dict, default_flow_style=False, sort_keys=False)

    if output_path:
        Path(output_path).write_text(yaml_str)

    return yaml_str
