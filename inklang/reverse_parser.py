def _extract_viewbox_and_size(svg_root: Any) -> tuple[float, float]:
    """Extract canvas dimensions from viewBox or width/height attributes.
    
    Returns (width, height) as floats. Prefers viewBox if present.
    """
    viewbox = svg_root.get("viewBox")
    if viewbox:
        parts = viewbox.split()
        if len(parts) >= 4:
            return float(parts[2]), float(parts[3])

    width = svg_root.get("width")
    height = svg_root.get("height")
    if width and height:
        # Remove units (px, cm, etc.)
        w_str = str(width).rstrip("abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ%")
        h_str = str(height).rstrip("abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ%")
        try:
            return float(w_str), float(h_str)
        except ValueError:
            pass

    raise ParseError("Could not determine canvas size from viewBox or width/height")


def _parse_style_dict(style_str: Optional[str]) -> Dict[str, str]:
    """Parse a CSS style string into a dict.
    
    Example: "fill: #fff; stroke: #000; stroke-width: 2px" ->
    {"fill": "#fff", "stroke": "#000", "stroke-width": "2px"}
    """
    result = {}
    if not style_str:
        return result
    for item in style_str.split(";"):
        item = item.strip()
        if ":" in item:
            key, val = item.split(":", 1)
            result[key.strip()] = val.strip()
    return result


def _get_paint_color(element: Any, attr: str, style_dict: Dict[str, str]) -> Optional[str]:
    """Extract a paint color from element attributes or style.
    
    Checks (in order): element attribute, style dict, returns None if neither.
    Normalizes 'none' to None.
    """
    # Check direct attribute first
    value = element.get(attr)
    if value and value.lower() != "none":
        return value

    # Check style dict (CSS names use dashes)
    style_key = attr if attr in style_dict else attr.replace("_", "-")
    value = style_dict.get(style_key)
    if value and value.lower() != "none":
        return value

    return None


def _get_numeric_style(
    element: Any, style_dict: Dict[str, str], attr: str, default: Optional[float] = None
) -> Optional[float]:
    """Extract a numeric value from element or style, stripping units."""
    # Try element attribute
    val = element.get(attr)
    if val:
        try:
            return float(str(val).rstrip("abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ%"))
        except ValueError:
            pass

    # Try style dict (CSS names use dashes)
    style_key = attr if attr in style_dict else attr.replace("_", "-")
    val = style_dict.get(style_key)
    if val:
        try:
            return float(str(val).rstrip("abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ%"))
        except ValueError:
            pass

    return default


def _extract_stroke(element: Any, style_dict: Dict[str, str]) -> Optional[Stroke]:
    """Extract stroke from element attributes and style, or return None if no stroke."""
    color = _get_paint_color(element, "stroke", style_dict)
    if not color:
        return None

    width = _get_numeric_style(element, style_dict, "stroke-width", default=1.0)
    dash_str = style_dict.get("stroke-dasharray")
    dash = None
    if dash_str:
        try:
            dash = [float(v.strip()) for v in dash_str.split()]
        except ValueError:
            pass

    return Stroke(color=color, width=width or 1.0, dash=dash)


def _parse_transform_string(transform_str: Optional[str]) -> Optional[TransformSpec]:
    """Parse SVG transform attribute into a TransformSpec.
    
    Handles translate, rotate, scale, skewX, skewY, matrix.
    This is a simplified parser; complex nested transforms may not round-trip perfectly.
    """
    if not transform_str:
        return None

    transform_str = transform_str.strip()
    spec_dict = {}

    # Extract individual transforms using regex-like parsing
    import re

    # Pattern: func_name(args)
    pattern = r"(\w+)\s*\(\s*([^)]+)\)"
    for match in re.finditer(pattern, transform_str):
        func_name = match.group(1).lower()
        args_str = match.group(2).strip()
        try:
            args = [float(x.strip()) for x in args_str.split(",") if x.strip()]
        except ValueError:
            continue

        if func_name == "translate" and len(args) >= 1:
            spec_dict["translate"] = args[:2] if len(args) >= 2 else [args[0], 0]
        elif func_name == "rotate" and len(args) >= 1:
            if len(args) == 1:
                spec_dict["rotate"] = args[0]
            elif len(args) == 3:
                spec_dict["rotate"] = {"angle": args[0], "center": [args[1], args[2]]}
        elif func_name == "scale" and len(args) >= 1:
            spec_dict["scale"] = args[0] if len(args) == 1 else args[:2]
        elif func_name == "skewx" and len(args) >= 1:
            spec_dict["skewX"] = args[0]
        elif func_name == "skewy" and len(args) >= 1:
            spec_dict["skewY"] = args[0]
        elif func_name == "matrix" and len(args) >= 6:
            spec_dict["matrix"] = " ".join(str(a) for a in args)

    if not spec_dict:
        return None

    return TransformSpec(**spec_dict)


def _extract_base_attrs(element: Any, style_dict: Dict[str, str]) -> Dict[str, Any]:
    """Extract shared ElementBase attributes: id, opacity, transform, clip, filter, mask."""
    attrs = {}

    if element.get("id"):
        attrs["id"] = element.get("id")

    opacity = _get_numeric_style(element, style_dict, "opacity")
    if opacity is not None and opacity != 1.0:
        attrs["opacity"] = opacity

    transform = element.get("transform")
    if transform:
        transform_spec = _parse_transform_string(transform)
        if transform_spec:
            attrs["transform"] = transform_spec

    clip_path = element.get("clip-path")
    if clip_path:
        # Extract ID from url(#id)
        import re
        match = re.search(r"url\(#([^)]+)\)", clip_path)
        if match:
            attrs["clip"] = match.group(1)

    mask_attr = element.get("mask")
    if mask_attr:
        import re
        match = re.search(r"url\(#([^)]+)\)", mask_attr)
        if match:
            attrs["mask"] = match.group(1)

    filter_attr = style_dict.get("filter")
    if filter_attr:
        import re
        match = re.search(r"url\(#([^)]+)\)", filter_attr)
        if match:
            attrs["filter"] = match.group(1)

    return attrs


def _parse_text_element(element: Any, style_dict: Dict[str, str]) -> Optional[TextModel]:
    """Parse a <text> element into a TextModel."""
    try:
        x = float(element.get("x", 0))
        y = float(element.get("y", 0))
        content = "".join(element.itertext()) or ""

        color = _get_paint_color(element, "fill", style_dict) or "#000000"

        font_family = style_dict.get("font-family", "Arial")
        font_size = _get_numeric_style(element, style_dict, "font-size", default=16)
        font_weight = style_dict.get("font-weight")
        font_style = style_dict.get("font-style")

        font = Font(
            family=font_family,
            size=int(font_size),
            weight=font_weight,
            style=font_style,
        )

        return TextModel(
            type="text",
            content=content,
            position=[x, y],
            color=color,
            font=font,
            **_extract_base_attrs(element, style_dict),
        )
    except Exception as e:
        raise ParseError(f"Failed to parse <text> element: {e}")


def _parse_rect_element(element: Any, style_dict: Dict[str, str]) -> Optional[RectElement]:
    """Parse a <rect> element into a RectElement."""
    try:
        x = float(element.get("x", 0))
        y = float(element.get("y", 0))
        width = float(element.get("width", 0))
        height = float(element.get("height", 0))

        fill = _get_paint_color(element, "fill", style_dict) or "#000000"
        stroke = _extract_stroke(element, style_dict)

        corner_radius = None
        rx = element.get("rx")
        if rx:
            try:
                corner_radius = float(rx)
            except ValueError:
                pass

        return RectElement(
            type="rect",
            position=[x, y],
            size=[width, height],
            fill=fill,
            stroke=stroke,
            corner_radius=corner_radius,
            **_extract_base_attrs(element, style_dict),
        )
    except Exception as e:
        raise ParseError(f"Failed to parse <rect> element: {e}")


