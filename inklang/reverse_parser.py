def _parse_polyline_element(element: Any, style_dict: Dict[str, str]) -> Optional[PolylineElement]:
    """Parse a <polyline> element into a PolylineElement."""
    try:
        points_str = element.get("points", "")
        points = []
        for pair in points_str.split():
            if "," in pair:
                x, y = pair.split(",")
                points.append([float(x), float(y)])

        stroke = _extract_stroke(element, style_dict)
        if not stroke:
            stroke = Stroke(color="#000000", width=1)

        fill = _get_paint_color(element, "fill", style_dict)

        return PolylineElement(
            type="polyline",
            points=points,
            stroke=stroke,
            fill=fill,
            **_extract_base_attrs(element, style_dict),
        )
    except Exception as e:
        raise ParseError(f"Failed to parse <polyline> element: {e}")


